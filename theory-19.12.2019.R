Проста линейна регресия

Y = b0 + b1X + E
E - грешка
X - независима променлива (предиктор)
У - зависима променлива (отклик)
b0 - ръст на бащите
b1 - ръст на синовете

X1, X2, ... Xn
Y1, Y2, ... Yn

yi = b0+b1xi
(Ei)^2 = (yi - bo - b1xi)^2

E = sum(i)(yi - b0 - b1xi)^2

b1^ = sum()((yi - y_)(xi - x_)) / sum(i)(xi - x_)^2
x_ = sum(i)(xi) / n

bo^ = y_ - b1x_

